package java_basics;

public class LearningArrays {
	
	public static void main(String[] args) {
		
		int [] values = new int[20];
		values[0] = 1000;
		values[19] = 10000;
		System.out.println(values[1]);
		System.out.println(values[0]);
		
		String [] names = new String[] {"me", "u", "i"};
		System.out.println(names[0]);
		
		names = new String[10];
		System.out.println(names[0]);
		
	}

}
